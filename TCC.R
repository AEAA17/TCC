install.packages("bibliometrix")
biblioshiny()


install.packages("rsm")
install.packages("lmtest")
library(rsm)
library(lmtest)
Ordem <- c(1, 2, 3, 4, 5, 6, 7)
T <- c(-1, -1, 1, 1, 0, 0, 0)
C <- c(-1, 1, -1, 1, 0, 0, 0)
y <- c(13419, 18950, 16381, 17675, 16422, 16673, 16852)
planej <- data.frame(Ordem, T, C, y)
print(planej)
modelo1 <- lm(y~T*C,data=planej)
summary(modelo1)
anov <- aov(modelo1)
summary(anov)
shapiro.test(modelo1$residuals) 
bptest(modelo1)
par(mfrow=c(1,1))
plot(modelo1) 
T1<-c(200,200,250,250,225,225,225)
C1<-c(0.1,0.3,0.1,0.3,0.2,0.2,0.2)
y <- c(13419, 18950, 16381, 17675, 16422, 16673, 16852)
modelo2 <-lm(y~T1*C1) 
persp(modelo2,C1~T1,zlab="MW",col=rainbow(50), contours="colors")
legend("bottomright",legend=c("C1−Catalisador","T1−Temperatura")
       ,cex=0.75, pt.cex=0.75)
